package oca.javaclasses;

public class MyJavaClass 
{
	public static void main(String[] args)
	{
		//primitive datatypes
		byte b=120;
		short s=384;
		int i=2567;
		double d=10.5;
		float f=10.8f;
		long l=85l;
		char c='m';
		boolean bo=true;
		char ch=90;
		int v=(int)10.5;
		System.out.println(b+","+s+","+i+","+d+","+f+","+l+","+c+","+bo+","+ch+","+v);
		//arithmetic operators
		int n=10,m=5,a;
		a=n+m;
		int k=n-m;
		int p=n/m;
		int o=n%m;
		int q=++n;
		int r=n++;
		int t=--m;
		int u=m--;
		n+=10;
		System.out.println(a+","+k+","+p+","+o+","+q+","+r+","+t+","+u+","+n);
		}
	

}
